   private void store(BufferedWriter bw, boolean escUnicode)
            throws IOException
        {
            bw.write("#" + new Date().toString());
            bw.newLine();
            synchronized (this) {
            	for (String key : keys()) {
                    String val = (String)getString(key, "");
                    key = saveConvert(key, true, escUnicode);
                    /* No need to escape embedded and trailing spaces for value, hence
                     * pass false to flag.
                     */
                    val = saveConvert(val, false, escUnicode);
                    bw.write(key + "=" + val);
                    bw.newLine();
                }
            }
            bw.flush();
        }

   private String saveConvert(String value, boolean escapeSpace, boolean escapeUnicode) {
		int len = value.length();
		int bufLen = len * 2;
		if (bufLen < 0) bufLen = Integer.MAX_VALUE;
		StringBuilder outBuffer = new StringBuilder(bufLen);
		for(int x=0; x<len; x++) {
			char aChar = value.charAt(x);
			if ((aChar > 61) && (aChar < 127)) {
				if (aChar == '\\') {
					outBuffer.append('\\'); outBuffer.append('\\');
					continue;
				}
				outBuffer.append(aChar);
				continue;
			}
			switch(aChar) {
			case ' ':
				if (x == 0 || escapeSpace)
					outBuffer.append('\\');
				outBuffer.append(' ');
				break;
			case '\t':outBuffer.append('\\'); outBuffer.append('t');
			break;
			case '\n':outBuffer.append('\\'); outBuffer.append('n');
			break;
			case '\r':outBuffer.append('\\'); outBuffer.append('r');
			break;
			case '\f':outBuffer.append('\\'); outBuffer.append('f');
			break;
			case '=': 
			case ':': 
			case '#': 
			case '!':
				outBuffer.append('\\'); outBuffer.append(aChar);
				break;
			default:
				if (((aChar < 0x0020) || (aChar > 0x007e)) & escapeUnicode ) {
				    outBuffer.append('\\');
				    outBuffer.append('u');
				    outBuffer.append(toHex((aChar >> 12) & 0xF));
				    outBuffer.append(toHex((aChar >>  8) & 0xF));
				    outBuffer.append(toHex((aChar >>  4) & 0xF));
				    outBuffer.append(toHex( aChar        & 0xF));
				} else {
					outBuffer.append(aChar);
				}
			}
		}
		return outBuffer.toString();
	}
   
   private static char toHex(int nibble) {
       return hexDigit[(nibble & 0xF)];
   }

