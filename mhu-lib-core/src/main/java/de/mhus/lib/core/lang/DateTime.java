package de.mhus.lib.core.lang;

import java.util.Date;

/**
 * <p>DateTime class.</p>
 *
 * @author mikehummel
 * @version $Id: $Id
 */
public class DateTime extends Date {

	/**
	 * <p>Constructor for DateTime.</p>
	 */
	public DateTime() {
		super();
	}

	/**
	 * <p>Constructor for DateTime.</p>
	 *
	 * @param date a long.
	 */
	public DateTime(long date) {
		super(date);
	}

}
