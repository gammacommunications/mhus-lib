	/**
	 * Return the amount of free not allocated memory
	 * @return memory in bytes
	 */
	public static long freeMemory() {
		Runtime r = Runtime.getRuntime();
		return r.maxMemory() - r.totalMemory() + r.freeMemory();
	}

